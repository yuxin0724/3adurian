function changeLanguage(lang) {
	currentLanguage = lang;
	localStorage.setItem('language', lang);
	
	// Add temporary CSS to smooth transition when changing languages
	const tempStyle = document.createElement('style');
	tempStyle.textContent = '[data-translate] { opacity: 0; transition: opacity 0.1s ease; }';
	document.head.appendChild(tempStyle);
	
	// Update current language display
	const langDisplay = {
		'en': 'ðŸ‡¬ðŸ‡§ EN',
		'ms': 'ðŸ‡²ðŸ‡¾ MS',
		'zh': 'ðŸ‡¨ðŸ‡³ ä¸­æ–‡'
	};
	const currentLangElement = document.getElementById('current-language');
	if (currentLangElement) {
		currentLangElement.textContent = langDisplay[lang];
	}
	
	// Update active state in dropdown
	document.querySelectorAll('.language-option').forEach(option => {
		option.classList.remove('active');
	});
	event.target.classList.add('active');
	
	// Translate all elements after short delay
	setTimeout(() => {
		translatePage(lang);
		
		// Remove temporary style to restore visibility
		setTimeout(() => {
			if (document.head.contains(tempStyle)) {
				document.head.removeChild(tempStyle);
			}
			// Set final opacity
			document.querySelectorAll('[data-translate]').forEach(el => {
				el.style.opacity = '1';
			});
		}, 50);
	}, 50);
	
	// Close dropdown
	const switcher = document.querySelector('.language-switcher');
	if (switcher) {
		switcher.classList.remove('active');
	}
}

function translatePage(lang) {
	const elements = document.querySelectorAll('[data-translate]');
	elements.forEach(element => {
		const key = element.getAttribute('data-translate');
		if (translations[lang] && translations[lang][key]) {
			element.textContent = translations[lang][key];
		}
	});
}

