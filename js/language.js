const antiFlickerStyle = document.createElement('style');
antiFlickerStyle.textContent = '[data-translate] { visibility: hidden; }';
document.head.appendChild(antiFlickerStyle);

// Initialize language as soon as possible
function initializeLanguage() {
	translatePage(currentLanguage);
	
	// Remove anti-flicker CSS after translation is applied
	document.head.removeChild(antiFlickerStyle);
	
	const langDisplay = {
		'en': 'ðŸ‡¬ðŸ‡§ EN',
		'ms': 'ðŸ‡²ðŸ‡¾ MS',
		'zh': 'ðŸ‡¨ðŸ‡³ ä¸­æ–‡'
	};
	const currentLangElement = document.getElementById('current-language');
	if (currentLangElement) {
		currentLangElement.textContent = langDisplay[currentLanguage];
	}
	
	// Set active language in dropdown
	document.querySelectorAll('.language-option').forEach(option => {
		option.classList.remove('active');
		const onclickStr = option.getAttribute('onclick');
		if (onclickStr && onclickStr.includes(currentLanguage)) {
			option.classList.add('active');
		}
	});
}

// Run immediately if DOM is ready, otherwise wait for it
if (document.readyState === 'loading') {
	document.addEventListener('DOMContentLoaded', initializeLanguage);
} else {
	initializeLanguage();
}

// Close dropdown when clicking outside
